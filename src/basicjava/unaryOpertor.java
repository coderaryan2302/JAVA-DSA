package basicjava;

public class unaryOpertor {
    public static void main(String[] args) {
        int p = 5, q = 10;

        System.out.println(p++);
        System.out.println(p--);
        System.out.println(--p);
        System.out.println(++p);
        System.out.println(q++);
        System.out.println(q--);
        System.out.println(++q);
        System.out.println(--q);

    }
}